    private void displayCourseListsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayCourseListsActionPerformed
        ArrayList<ScheduleEntry> scheduledStudents = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, (String) courseListAdminCoursesCB.getSelectedItem());
        ArrayList<ScheduleEntry> waitlistedStudents = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, (String) courseListAdminCoursesCB.getSelectedItem());

        DefaultTableModel currentTableModel = (DefaultTableModel) scheduledStudentsTable.getModel();
        currentTableModel.setNumRows(0);
        Object[] outputData = new Object[3];
        for (ScheduleEntry s : scheduledStudents) {
            StudentEntry current = StudentQueries.getStudent(s.getStudentID());
            outputData[0] = current.getLastName();
            outputData[1] = current.getFirstName();
            outputData[2] = s.getStudentID();
            currentTableModel.addRow(outputData);
        }

        currentTableModel = (DefaultTableModel) waitlistedStudentsTable.getModel();
        currentTableModel.setNumRows(0);
        outputData = new Object[3];
        for (ScheduleEntry s : waitlistedStudents) {
            StudentEntry current = StudentQueries.getStudent(s.getStudentID());
            outputData[0] = current.getLastName();
            outputData[1] = current.getFirstName();
            outputData[2] = s.getStudentID();
            currentTableModel.addRow(outputData);
        }
    }//GEN-LAST:event_displayCourseListsActionPerformed

    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        dropStudentTextArea.setText("");
        StudentEntry currentStudent = StudentQueries.getStudent(studentNamesCB(dropStudentCB)[1]);
        dropStudentTextArea.append((String) dropStudentCB.getSelectedItem() + " has been dropped from the list of stuents.\n\n");

        for (String currentSemester : SemesterQueries.getSemesterList()) {
            String droppedCourses = "";
            String scheduledCourses = "";
            for (ScheduleEntry currentEntry : ScheduleQueries.getScheduleByStudent(currentSemester, currentStudent.getStudentID())) {
                ScheduleQueries.dropStudentScheduleByCourse(currentSemester, currentStudent.getStudentID(), currentEntry.getCourseCode());
                droppedCourses += ((String) dropStudentCB.getSelectedItem() + (currentEntry.getStatus().equals("w") ? " has been dropped from the waitlist for " : " has been dropped from ") + currentEntry.getCourseCode() + "\n");

                ArrayList<ScheduleEntry> waitlistedStudents = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, currentEntry.getCourseCode());
                if (waitlistedStudents.size() > 0) {
                    ScheduleQueries.updateScheduleEntry(currentSemester, waitlistedStudents.get(0));
                    StudentEntry movedStudent = StudentQueries.getStudent(waitlistedStudents.get(0).getStudentID());
                    scheduledCourses += movedStudent.getLastName() + ", " + movedStudent.getLastName() + " " + waitlistedStudents.get(0).getStudentID() + " has been scheduled into " + waitlistedStudents.get(0).getCourseCode() + "\n";
                }
            }
            if (!droppedCourses.isEmpty() || !scheduledCourses.isEmpty()) {
                dropStudentTextArea.append("For Semester: " + currentSemester + "\n");
                dropStudentTextArea.append(droppedCourses);
                dropStudentTextArea.append(scheduledCourses);
            }
        }
        StudentQueries.dropStudent(studentNamesCB(dropStudentCB)[1]);
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_dropStudentButtonActionPerformed

    private void dropCourseStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropCourseStudentActionPerformed
        dropCourseStudentTextArea.setText("");
        String dropCourse = (String) dropCourseCB.getSelectedItem();
        ScheduleQueries.dropStudentScheduleByCourse(currentSemester, currentStudentSelected.getStudentID(), dropCourse);
        dropCourseStudentTextArea.append(currentStudentSelected.fullID() + " has been dropped from " + dropCourse + "\n");
        ArrayList<ScheduleEntry> waitlistedStudents = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, dropCourse);
        if (waitlistedStudents.size() > 0) {
            ScheduleQueries.updateScheduleEntry(currentSemester, waitlistedStudents.get(0));
            dropCourseStudentTextArea.append(StudentQueries.getStudent(waitlistedStudents.get(0).getStudentID()).lastFirstName() + " has been scheduled into " + dropCourse + "\n");
        }
        displayScheduleButtonActionPerformed(null);
        rebuildCurrentStudentCourseComboBox();
    }//GEN-LAST:event_dropCourseStudentActionPerformed

