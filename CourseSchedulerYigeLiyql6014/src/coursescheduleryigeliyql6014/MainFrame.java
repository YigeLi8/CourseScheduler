    public void rebuildSemesterComboBoxes() {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(
                semesters.toArray()));
        if (semesters.size() > 0) {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }

    public void rebuildStudentComboBoxes() {
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        ArrayList<String> studentNames = new ArrayList<>();
        ArrayList<String> courses = CourseQueries.getAllCourseCodes(
                currentSemester);
        selectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(
                courses.toArray()));

        for (StudentEntry student : students) {
            studentNames.add(student.getLastName() + ", " + student.getFirstName());
        }
        selectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(
                studentNames.toArray()));
        displayStudentScheduleComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));

    }

