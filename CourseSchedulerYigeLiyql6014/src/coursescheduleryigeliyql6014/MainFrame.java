    private void addCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCourseButtonActionPerformed
        if (courseCodeEntry.getText().length() > 0 && courseDescriptionEntry.getText().
                length() > 0) {
            CourseEntry newCourseEntry = new CourseEntry(currentSemester,
                    courseCodeEntry.getText(),
                    courseDescriptionEntry.getText(), (int) seatsEntry.getValue());
            CourseQueries.addCourse(newCourseEntry);
            addCourseOutput.setText(courseCodeEntry.getText() + " has been "
                    + "added.");
            displayCoursesButtonActionPerformed(null);
        }
    }//GEN-LAST:event_addCourseButtonActionPerformed

    private void addStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStudentButtonActionPerformed
        if (studentIDEntry.getText().length() > 0
                && firstNameEntry.getText().length() > 0
                && lastNameEntry.getText().length() > 0) {
            StudentEntry newStudent = new StudentEntry(
                    studentIDEntry.getText(),
                    firstNameEntry.getText(),
                    lastNameEntry.getText());
            StudentQueries.addStudent(newStudent);
            addStudentOutput.setText(lastNameEntry.getText() + ", "
                    + firstNameEntry.getText() + " has been added");
            rebuildStudentComboBoxes();
        }
    }//GEN-LAST:event_addStudentButtonActionPerformed

    private void displayCoursesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayCoursesButtonActionPerformed
        ArrayList<CourseEntry> courses = CourseQueries.getAllCourses(
                currentSemester);
        DefaultTableModel currentTableModel = (DefaultTableModel) coursesTable.getModel();
        currentTableModel.setNumRows(0);
        Object[] rowData = new Object[3];
        for (CourseEntry course : courses) {
            rowData[0] = course.getCourseCode();
            rowData[1] = course.getDescription();
            rowData[2] = course.getSeats();
            currentTableModel.addRow(rowData);
        }
    }//GEN-LAST:event_displayCoursesButtonActionPerformed

    private void submitScheduleCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitScheduleCourseButtonActionPerformed
        ArrayList<String> coursecodes = CourseQueries.getAllCourseCodes(
                currentSemester);
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();

        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(
                Calendar.getInstance().getTime().getTime());
        int currentStudentIndex = selectStudentComboBox.getSelectedIndex();
        int currentCourseIndex = selectCourseComboBox.getSelectedIndex();
        String currentCourse = coursecodes.get(currentCourseIndex);

        int scheduledStudents = ScheduleQueries.getScheduledStudentCount(
                currentSemester, currentCourse);
        if (scheduledStudents < CourseQueries.getCourseSeats(
                currentSemester, currentCourse)) {
            ScheduleQueries.addScheduleEntry(new ScheduleEntry(
                    currentSemester, currentCourse,
                    students.get(currentStudentIndex).getStudentID(),
                    "S", currentTimestamp));
            scheduleCourseOutputLabel.setText(students.get(
                    currentStudentIndex).getLastName() + ", "
                    + students.get(currentStudentIndex).getFirstName()
                    + " has been scheduled for the class");
        } else {
            ScheduleQueries.addScheduleEntry(new ScheduleEntry(
                    currentSemester, currentCourse,
                    students.get(currentStudentIndex).getStudentID(),
                    "W", currentTimestamp));
            scheduleCourseOutputLabel.setText(students.get(
                    currentStudentIndex).getLastName() + ", "
                    + students.get(currentStudentIndex).getFirstName()
                    + " has been waitlisted for the class");
        }
    }//GEN-LAST:event_submitScheduleCourseButtonActionPerformed

    private void displayScheduleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayScheduleButtonActionPerformed
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String studentID = students.get(
                displayStudentScheduleComboBox.getSelectedIndex()).getStudentID();
        ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(currentSemester, studentID);

        DefaultTableModel displayScheduleTableModel = (DefaultTableModel) displayStudentScheduleTable.getModel();

        displayScheduleTableModel.setNumRows(0);
        Object[] rowData = new Object[2];
        for (ScheduleEntry entry : schedule) {
            rowData[0] = entry.getCourseCode();
            rowData[1] = entry.getStatus();
            displayScheduleTableModel.addRow(rowData);
        }
    }//GEN-LAST:event_displayScheduleButtonActionPerformed

