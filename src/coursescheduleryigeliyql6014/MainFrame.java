    private void submitScheduleCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitScheduleCourseButtonActionPerformed

        String status = "";
        if (CourseQueries.getCourseSeats(currentSemester, (String) selectCourseCB.getSelectedItem()) > ScheduleQueries.getScheduledStudentCount(currentSemester, (String) selectCourseCB.getSelectedItem())) {
            status = "s";
            scheduleCourseOutputLabel.setText(currentStudentSelected.lastFirstName() + " has been scheduled for " + (String) selectCourseCB.getSelectedItem());
        } else {
            status = "w";
            scheduleCourseOutputLabel.setText(currentStudentSelected.lastFirstName() + " has been waitlisted for " + (String) selectCourseCB.getSelectedItem());
        }
        ScheduleEntry newEntry = new ScheduleEntry(currentSemester, (String) selectCourseCB.getSelectedItem(), currentStudentSelected.getStudentID(), status, new java.sql.Timestamp(System.currentTimeMillis()));
        ScheduleQueries.addScheduleEntry(newEntry);
        rebuildCurrentStudentCourseComboBox();
    }//GEN-LAST:event_submitScheduleCourseButtonActionPerformed

