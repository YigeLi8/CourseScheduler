    public void rebuildSemesterComboBoxes() {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterCB.setModel(new javax.swing.DefaultComboBoxModel(
                semesters.toArray()));
        if (semesters.size() > 0) {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }

    public void rebuildStudentComboBoxes() {
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String[] nameArray = new String[students.size()];
        int i = 0;
        for (StudentEntry student : students) {
            nameArray[i] = student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID();
            i++;
        }

        currentStudentSelectedCB.setModel(new javax.swing.DefaultComboBoxModel(nameArray));
        dropStudentCB.setModel(new javax.swing.DefaultComboBoxModel(nameArray));

        if (!students.isEmpty()) {
            currentStudentSelected = students.get(0);
            currentStudentSelectedLabel.setText(currentStudentSelected.fullID());
            rebuildCurrentStudentCourseComboBox();
        }

    }

    private void rebuildCurrentStudentCourseComboBox() {
        ArrayList<ScheduleEntry> courses = ScheduleQueries.getScheduleByStudent(currentSemester, currentStudentSelected.getStudentID());
        String[] coursesArray = new String[courses.size()];
        int i = 0;
        for (ScheduleEntry course : courses) {
            coursesArray[i] = course.getCourseCode();
            i++;
        }
        dropCourseCB.setModel(new javax.swing.DefaultComboBoxModel(coursesArray));
    }

    private void rebuildCourseComboBoxes() {
        selectCourseCB.setModel(new javax.swing.DefaultComboBoxModel(CourseQueries.getAllCourseCodes(currentSemester).toArray()));
        courseListAdminCoursesCB.setModel(new javax.swing.DefaultComboBoxModel(CourseQueries.getAllCourseCodes(currentSemester).toArray()));
        dropCoursesCB.setModel(new javax.swing.DefaultComboBoxModel(CourseQueries.getAllCourseCodes(currentSemester).toArray()));
    }

